function PlotImag(AxesHandle,TypeImage,name)
	% Function to plot an image in the current axes
	% depending on the name of the image
	% Modified by Emily H. Moore, 5/23/96
	% drawing the image
cla
x = TypeImage(1,:);
y = TypeImage(2,:);
if name == 'poly'
	fill([x(1:2),0],[y(1:2),0],'y',[x(2:3),0],[y(2:3),0],'m',...
	[x(3:4),0],[y(3:4),0],'c',[x(4:5),0],[y(4:5),0],'r',...
	[x(5:6),0],[y(5:6),0],'g',[x(6:7),0],[y(6:7),0],'w'),...
elseif name == 'star'
	fill([x(1:3),0],[y(1:3),0],'r',[x(3:5),0],[y(3:5),0],'y',...
	[x(5:7),0],[y(5:7),0],'c',...
	[x(7:8),x(1),0],[y(7:8),y(1),0],'g'),...
elseif name == 'pump'
	fill(x(1:315),y(1:315),'r',x(316:376),y(316:376),'w',...
	x(377:691),y(377:691),'m',x(692:1006),y(692:1006),'g',...
	x(1007:1107),y(1007:1107),'y')
else
	fill(x,y,'g'),...
end;
stretch